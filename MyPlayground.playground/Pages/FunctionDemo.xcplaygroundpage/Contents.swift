import Foundation

func calculate(number1 n1: Int, number2 n2: Int) -> Int {
    return n1 + n2
}

func add(n1: Int, n2: Int) -> Int {
    return n1 + n2
}

func sub(_ number1: Int, _ number2: Int) -> Int {
    return number1 - number2
}

print(calculate(number1: 10, number2: 20))
print(add(n1: 10, n2: 20))
print(sub(10, 20))

